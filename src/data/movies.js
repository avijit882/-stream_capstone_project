// All unique genres across the catalogue
export const ALL_GENRES = [...new Set(MOVIES.flatMap(m => m.genre))].sort();

// Get similar movies (same genre, different id)
export const getSimilar = (movie, count = 8) =>
  MOVIES.filter(m => m.id !== movie.id && m.genre.some(g => movie.genre.includes(g))).slice(0, count);

// Search movies
export const searchMovies = (query) => {
  const q = query.toLowerCase();
  return MOVIES.filter(m =>
    m.title.toLowerCase().includes(q) ||
    m.genre.some(g => g.toLowerCase().includes(q)) ||
    m.cast.some(c => c.toLowerCase().includes(q)) ||
    m.director.toLowerCase().includes(q)
  );
};

// Filter by genre
export const getByGenre = (genre) =>
  genre === 'All' ? MOVIES : MOVIES.filter(m => m.genre.includes(genre));
